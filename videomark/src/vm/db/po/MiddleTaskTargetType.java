package vm.db.po;

public class MiddleTaskTargetType {
	private String taskTargetTypeID;

	private long ctime;

	private long utime;

	private String note;

	private int flag;

	private String taskID;

	private String targetTypeID;

	public String getTaskTargetTypeID() {
		return taskTargetTypeID;
	}

	public void setTaskTargetTypeID(String taskTargetTypeID) {
		this.taskTargetTypeID = taskTargetTypeID;
	}

	public long getCtime() {
		return ctime;
	}

	public void setCtime(long ctime) {
		this.ctime = ctime;
	}

	public long getUtime() {
		return utime;
	}

	public void setUtime(long utime) {
		this.utime = utime;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	public String getTaskID() {
		return taskID;
	}

	public void setTaskID(String taskID) {
		this.taskID = taskID;
	}

	public String getTargetTypeID() {
		return targetTypeID;
	}

	public void setTargetTypeID(String targetTypeID) {
		this.targetTypeID = targetTypeID;
	}

}
